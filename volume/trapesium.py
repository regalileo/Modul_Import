def volume_trapesium():
    alas_atas = int(input("Masukan Nilai Alas_Atas :"))
    alas_bawah = int(input("Masukan Nilai Alas_Bawah :"))
    tinggi = int(input("Masukan Nilai Tinggi :"))
    panjang = int(input("Masukan Nilai Panjang :"))
    return 1/2 * (alas_bawah + alas_atas) * tinggi * panjang